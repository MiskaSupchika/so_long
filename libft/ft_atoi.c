#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	h;
	int	n;

	i = 0;
	h = 0;
	n = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '-')
		h = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
		n = n * 10 + (str[i++] - 48);
	if (h == -1)
		return (-n);
	return (n);
}
